import requests
from bs4 import BeautifulSoup
import json
import re
from datetime import datetime
from models import get_db_session, Prediction, init_db

def parse_btts_predictions():
    """–ü–∞—Ä—Å–∏—Ç –ø—Ä–æ–≥–Ω–æ–∑—ã '–û–±–µ –∑–∞–±—å—é—Ç' —Å –ø—Ä–∞–≤–∏–ª—å–Ω–æ–≥–æ URL"""
    print("–ù–∞—á–∏–Ω–∞–µ–º –ø–∞—Ä—Å–∏–Ω–≥ –ø—Ä–æ–≥–Ω–æ–∑–æ–≤...")
    
    try:
        # –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π URL –¥–ª—è –ø–∞—Ä—Å–∏–Ω–≥–∞
        url = "https://scores24.live/ru/trends?trendsMarketSlug=btts"
        headers = {
            'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36',
            'Accept': 'text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8',
            'Accept-Language': 'ru-RU,ru;q=0.8,en-US;q=0.5,en;q=0.3',
            'Connection': 'keep-alive'
        }
        
        # –î–µ–ª–∞–µ–º –∑–∞–ø—Ä–æ—Å –∫ —Å–∞–π—Ç—É
        print(f"–ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º URL: {url}")
        response = requests.get(url, headers=headers, timeout=30)
        response.raise_for_status()
        
        print(f"‚úì –°—Ç—Ä–∞–Ω–∏—Ü–∞ –∑–∞–≥—Ä—É–∂–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ (—Å—Ç–∞—Ç—É—Å: {response.status_code})")
        
        # –ü–∞—Ä—Å–∏–º HTML
        soup = BeautifulSoup(response.content, 'html.parser')
        
        # –ò—â–µ–º script —Ç–µ–≥ —Å URQL_DATA
        print("–ò—â–µ–º window.URQL_DATA...")
        script_tags = soup.find_all('script')
        
        target_script = None
        for i, script in enumerate(script_tags):
            if script.string and 'window.URQL_DATA' in script.string:
                target_script = script
                print(f"‚úì –ù–∞–π–¥–µ–Ω —Ü–µ–ª–µ–≤–æ–π script —Ç–µ–≥ —Å URQL_DATA")
                break
        
        if not target_script:
            print("‚úó –ù–µ –Ω–∞–π–¥–µ–Ω script —Ç–µ–≥ —Å window.URQL_DATA")
            return 0
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –∏ –ø–∞—Ä—Å–∏–º JSON –¥–∞–Ω–Ω—ã–µ
        print("–ü–∞—Ä—Å–∏–º JSON –¥–∞–Ω–Ω—ã–µ –∏–∑ URQL_DATA...")
        script_content = target_script.string.strip()
        
        # –ò–∑–≤–ª–µ–∫–∞–µ–º JSON —Å—Ç—Ä–æ–∫—É
        json_str = script_content.split('window.URQL_DATA=JSON.parse("', 1)[1]
        json_str = json_str.rsplit('");', 1)[0]
        
        # –î–µ–∫–æ–¥–∏—Ä—É–µ–º —ç–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã
        json_str = json_str.encode('utf-8').decode('unicode_escape')
        
        # –ü–∞—Ä—Å–∏–º JSON
        urql_data = json.loads(json_str)
        
        # –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∞–Ω–Ω—ã—Ö
        print("–ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É URQL_DATA...")
        
        # –ò—â–µ–º –∫–ª—é—á–∏ –≤ –¥–∞–Ω–Ω—ã—Ö
        first_key = list(urql_data.keys())[0]
        print(f"–ü–µ—Ä–≤—ã–π –∫–ª—é—á –≤ URQL_DATA: {first_key}")
        
        # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ –ø–µ—Ä–≤–æ–º—É –∫–ª—é—á—É
        trend_data = urql_data[first_key]
        if 'data' in trend_data:
            trend_data = json.loads(trend_data['data'])
        
        print(f"–ö–ª—é—á–∏ –≤ trend_data: {list(trend_data.keys())}")
        
        # –ò—â–µ–º –º–∞—Ç—á–∏ –≤ —Å—Ç—Ä—É–∫—Ç—É—Ä–µ
        matches_data = []
        
        if 'TrendFilter' in trend_data:
            trend_filter = trend_data['TrendFilter']
            if 'matches' in trend_filter:
                matches_data = trend_filter['matches']
                print(f"‚úì –ù–∞–π–¥–µ–Ω–æ –º–∞—Ç—á–µ–π: {len(matches_data)}")
        
        if not matches_data:
            print("‚úó –ù–µ —É–¥–∞–ª–æ—Å—å –Ω–∞–π—Ç–∏ –º–∞—Ç—á–∏ –≤ —Å—Ç—Ä—É–∫—Ç—É—Ä–µ –¥–∞–Ω–Ω—ã—Ö")
            print("–î–æ—Å—Ç—É–ø–Ω—ã–µ –∫–ª—é—á–∏:")
            for key in trend_data.keys():
                print(f"  {key}")
                if key == 'TrendFilter':
                    for subkey in trend_data[key].keys():
                        print(f"    {subkey}")
            return 0
        
        # –ü–æ–¥–∫–ª—é—á–∞–µ–º—Å—è –∫ –ë–î
        session = get_db_session()
        new_count = 0
        
        print(f"\n–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º {len(matches_data)} –º–∞—Ç—á–µ–π...")
        
        # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∫–∞–∂–¥—ã–π –º–∞—Ç—á
        for i, match in enumerate(matches_data[:10]):  # –ü–µ—Ä–≤—ã–µ 10 –¥–ª—è —Ç–µ—Å—Ç–∞
            try:
                print(f"\n--- –ú–∞—Ç—á {i+1} ---")
                
                # –ò–∑–≤–ª–µ–∫–∞–µ–º –¥–∞–Ω–Ω—ã–µ –æ –∫–æ–º–∞–Ω–¥–∞—Ö
                home_team = match.get('homeTeam', {}).get('name', 'Unknown Home')
                away_team = match.get('awayTeam', {}).get('name', 'Unknown Away')
                
                # –ò–∑–≤–ª–µ–∫–∞–µ–º –≤—Ä–µ–º—è –º–∞—Ç—á–∞
                match_time = None
                if 'startTime' in match:
                    match_time = datetime.fromtimestamp(match['startTime'])
                else:
                    match_time = datetime.now()
                
                # –ò–∑–≤–ª–µ–∫–∞–µ–º –ø—Ä–æ–≥–Ω–æ–∑ (–∫–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç)
                prediction_value = "Yes"  # –ü–æ —É–º–æ–ª—á–∞–Ω–∏—é
                if 'trend' in match and 'odds' in match['trend']:
                    prediction_value = str(match['trend']['odds'])
                
                print(f"–ö–æ–º–∞–Ω–¥—ã: {home_team} vs {away_team}")
                print(f"–í—Ä–µ–º—è: {match_time}")
                print(f"–ö–æ—ç—Ñ—Ñ–∏—Ü–∏–µ–Ω—Ç: {prediction_value}")
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ —Ç–∞–∫–æ–π –º–∞—Ç—á –≤ –ë–î
                existing = session.query(Prediction).filter_by(
                    home_team=home_team,
                    away_team=away_team, 
                    match_time=match_time
                ).first()
                
                if not existing:
                    # –°–æ–∑–¥–∞–µ–º –Ω–æ–≤—É—é –∑–∞–ø–∏—Å—å
                    new_prediction = Prediction(
                        home_team=home_team,
                        away_team=away_team,
                        match_time=match_time,
                        prediction_value=prediction_value,
                        source="scores24.live"
                    )
                    session.add(new_prediction)
                    new_count += 1
                    print(f"‚úì –î–æ–±–∞–≤–ª–µ–Ω –≤ –ë–î: {home_team} vs {away_team}")
                else:
                    print(f"‚úì –£–∂–µ –≤ –ë–î: {home_team} vs {away_team}")
                    
            except Exception as e:
                print(f"‚úó –û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –º–∞—Ç—á–∞: {e}")
                continue
        
        # –°–æ—Ö—Ä–∞–Ω—è–µ–º –≤—Å–µ –∏–∑–º–µ–Ω–µ–Ω–∏—è
        session.commit()
        session.close()
        
        print(f"\nüéâ –ì–æ—Ç–æ–≤–æ! –î–æ–±–∞–≤–ª–µ–Ω–æ {new_count} –Ω–æ–≤—ã—Ö –ø—Ä–æ–≥–Ω–æ–∑–æ–≤")
        return new_count
        
    except Exception as e:
        print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–∞—Ä—Å–∏–Ω–≥–µ: {e}")
        import traceback
        traceback.print_exc()
        return 0

if __name__ == "__main__":
    # –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º –ë–î
    init_db()
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º –ø–∞—Ä—Å–∏–Ω–≥
    parse_btts_predictions()
